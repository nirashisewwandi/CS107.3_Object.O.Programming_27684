using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleApp27
{
    class CalculateValues
    {
        public static int Addition(int no1, int no2)
        {
            return no1 + no2;
        }

        public static int Subtraction(int no1, int no2)
        {
            return no1 - no2;
        }

        public static int Multiplication(int no1, int no2)
        {
            return no1 * no2;
        }

        public static int Division(int no1, int no2)
        {
            return no1 / no2;
        }
    }
}
